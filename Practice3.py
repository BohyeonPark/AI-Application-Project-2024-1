#추가 응용자료
print("안녕하세요") #문자
print("100") #문자
print("%d" %100) #10진수

print("100+100") #문자
print("%d" %(100+100)) #10진수 사칙연산
print("%d %d" %(100,100)) #10진수 나열

#자리수 지정하기
print("%d" %123) #10진수
print("%5d" %123) #10진수, 값을 5자리 확보하여 출력

print("%f" %123.45) #실수
print("%5.1f" %123.45) #실수, 값을 5자리 확보하고 소수점 1까지 출력
print("%5.2f" %123.45) #실수, 값을 5자리 확보하고 소수점 2까지 출력

print("%s" %"Hello") #문자열
print("%7s" %"Hello") #문자열, 값을 7자리 확보하여 출력
